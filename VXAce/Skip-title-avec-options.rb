#==============================================================================
# ** Configuration
#------------------------------------------------------------------------------
#  Configuration of the title_screen
#==============================================================================
module Config
	#--------------------------------------------------------------------------
  	# * Map Id of the title
  	#--------------------------------------------------------------------------
	MAP_ID = 1
	#--------------------------------------------------------------------------
  	# * X position of the heroes
  	#--------------------------------------------------------------------------
	COORD_X = 1
	#--------------------------------------------------------------------------
  	# * Y position of the heroes
  	#--------------------------------------------------------------------------
	COORD_Y = 2
	#--------------------------------------------------------------------------
  	# * Activation of the switch title
  	#--------------------------------------------------------------------------
	ACTIVATE = true
end

#==============================================================================
# ** Scene_Title
#------------------------------------------------------------------------------
#  This class performs the title screen processing.
#==============================================================================
class Scene_Title < Scene_Base
	#--------------------------------------------------------------------------
  	# * Alias
  	#--------------------------------------------------------------------------
	alias title_start start
	#--------------------------------------------------------------------------
    # * Start Processing
    #--------------------------------------------------------------------------
	def start
		unless Config::ACTIVATE
			title_start
		else
			super
			DataManager.create_game_objects
			$game_party.setup_starting_members
			$game_map.setup(Config::MAP_ID)
			$game_map.autoplay
			$game_player.moveto(Config::COORD_X, Config::COORD_Y)
			$game_player.refresh
			SceneManager.goto(Scene_Map)
		end
	end
	#--------------------------------------------------------------------------
  	# * Free Background
 	#--------------------------------------------------------------------------
	def dispose_background
		if @sprite1
			@sprite1.bitmap.dispose 
			@sprite1.dispose 
			@sprite2.bitmap.dispose
			@sprite2.dispose 
		end
	end
	#--------------------------------------------------------------------------
  	# * Free Foreground
  	#--------------------------------------------------------------------------
	def dispose_foreground
		if @foreground_sprite
			@foreground_sprite.bitmap.dispose
			@foreground_sprite.dispose
		end
	end
end

